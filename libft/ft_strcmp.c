#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	size_t	i;

	if (s1 && s2)
	{
		i = 0;
		while (s1[i] == s2[i] && s1[i])
			i++;
		return (s1[i] - s2[i]);
	}
	if (!s1 && !s2)
		return (0);
	return (0);
}
